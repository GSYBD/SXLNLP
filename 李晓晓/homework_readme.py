#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    def dfs(start, path):
        if start == len(sentence):
            # 当遍历到字符串末尾时，将当前路径添加到结果中
            target.append(path[:])
            return

        for i in range(start, len(sentence)):
            word = sentence[start:i + 1]
            if word in Dict:
                # 如果当前片段在词典中，则继续向后搜索
                path.append(word)
                dfs(i + 1, path)
                path.pop()
    target = []
    dfs(0, [])
    return target

for cut in all_cut(sentence, Dict):
    print("[" + ", ".join(f"'{word}'" for word in cut) + "]")

