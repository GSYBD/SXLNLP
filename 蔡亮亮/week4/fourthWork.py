def _cut(s, start, path, result, Dict):
    if start == len(s):
        result.append(path[:])
        return
    for end in range(start + 1, len(s) + 1):
        word = s[start:end]
        if word in Dict:
            path.append(word)
            _cut(s, end, path, result,Dict)
            path.pop()
