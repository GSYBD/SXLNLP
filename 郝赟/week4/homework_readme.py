def all_cut(sentence, Dict):
    def recursive_cut(s, Dict, path, result):
        if not s:
            result.append(path)
            return
        for i in range(1, len(s) + 1):
            word = s[:i]
            if word in Dict:
                recursive_cut(s[i:], Dict, path + [word], result)
    result = []
    recursive_cut(sentence, Dict, [], result)
    return result

