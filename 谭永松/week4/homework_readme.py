# 递归进行全切分
def cut(sentence, Dict, current_cut=[], all_cuts=[]):
    if not sentence:
        all_cuts.append(current_cut)
        return all_cuts

    for word in Dict:
        if sentence.startswith(word):
            cut(sentence[len(word):], Dict, current_cut + [word], all_cuts)
    return all_cuts


